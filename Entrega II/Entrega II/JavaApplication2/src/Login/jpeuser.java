    private void btsaveepeuserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsaveepeuserActionPerformed
            SqlUsuarios modSql  = new SqlUsuarios();
            usuarios    mod     = new usuarios(); 
                                     
            String ids = new String(txtid.getText());
            String nombre = new String(txtnombres.getText());
            String apellido = new String(txtapellido.getText());
            String password = new String(txtpassword.getText());
            String email = new String(txtemail.getText());
            String user = new String(txtuser.getText());
            String direccion = new String(txtdireccion.getText());           
            String tel = new String(txttelefono.getText());   
            
            
              
            int id = Integer.parseInt(ids);
           
            
            
            
            mod.setId(id);
            mod.setNombre(nombre);
            mod.setApellido(apellido);
            mod.setPassword(password);
            mod.setEmail(email);
            mod.setUsuario(user);
            mod.setDireccion(direccion);
            mod.setTelefono(tel);        
           
            
            modSql.registrar(mod);
            
            limpiar();
               
    }//GEN-LAST:event_btsaveepeuserActionPerformed

